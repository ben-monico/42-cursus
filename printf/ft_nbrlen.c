int ft_nbrlen(long n, int base)
{
	int	len;
	len = 1;
	if (n == -4294967296)
		return (11);
	if (n < 0)
	{
		n = -n;
		len++;
	}
	while (n >= base)
	{
		len++;
		n /= base;
	}
	return (len);
}